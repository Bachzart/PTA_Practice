#include <cstdio>
int N, D;
double e;

int main() {
	scanf("%d %lf %d", &N, &e, &D);
	int empty = 0, possible = 0, tmp = N;
	while(tmp--) {
		int k, half = 0;
		scanf("%d", &k);
		double days[k];
		for(int i = 0; i < k; i++) {
			scanf("%lf", &days[i]);
			if(days[i] < e) half++;
		}
		if(half > k / 2) {
			if(k > D) empty++;
			else possible++;
		}
	}
	printf("%.1lf%% %.1lf%%\n", possible * 100.0 / N, empty * 100.0 / N);
	return 0;
} 

